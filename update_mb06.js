const { createClient } = require('@supabase/supabase-js');
const dotenv = require('dotenv');
dotenv.config({ path: '.env.local' });

async function run() {
  const supabase = createClient(process.env.NEXT_PUBLIC_SUPABASE_URL, process.env.SUPABASE_SERVICE_ROLE_KEY);
  
  const desc = 'MB06 предлага отлични условия за създаване на мобилни жилищни или работни пространства, като съчетава висока енергийна ефективност с просторни възможности за адаптация. Този модел е проектиран за тези, които търсят по-големи пространства с оптимална изолация, които да могат да се използват както за жилищни, така и за офис нужди.\n\nMB06 е изграден с надеждни материали, които гарантират дълготрайност и издръжливост. Проектиран за различни климатични условия, той предлага комфорт през всички сезони, което го прави подходящ за строителни обекти, мобилни селища и временни офис помещения.\n\nПредимства на MB06:\n- Просторен интериор: Осигурява комфорт за по-големи екипи или семейства.\n- Енергоефективност: Намалява разходите за енергия чрез иновативни изолационни технологии.\n- Издръжлив и устойчив: Подходящ за всякакви климатични условия.\n\nПриложение на MB06:\n- Може да се използва за изграждане на мобилни селища и временно настаняване на персонал.\n- Идеален за офис пространства и работни помещения на строителни обекти.\n- Подходящ за индивидуални нужди или малки семейства, които търсят мобилност и комфорт.';
  
  const specs = { 
    'Площ (м2)': '14.4', 
    'Дължина (мм)': '6000', 
    'Ширина (мм)': '2400', 
    'Височина (мм)': '2800', 
    'Товароносимост': '500 кг/м2', 
    'Дебелина на панела': '50 мм', 
    'Врата': '900x2000 мм' 
  };

  const { data, error } = await supabase
    .from('products')
    .update({ description: desc, specs: specs })
    .ilike('name', '%MB06%');

  if (error) console.error(error);
  else console.log('MB06 updated successfully');
}
run();
